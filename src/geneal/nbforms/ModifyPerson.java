    public ModifyPerson(db.persona p) {
        initComponents();
        
        fnaixement = new formLloc(this.c_municipi, this.c_parroquia, this.c_llogaret);
        fdef = new formLloc(this.c_dmunicipi, this.c_dparroquia, this.c_dllogaret);
        dnaixement = new formData(this.c_dia, this.c_mes, this.t_any);
        ddef = new formData(this.c_ddia, this.c_dmes, this.t_dany);
        
        javax.swing.JTextField[] nom = {this.t_nom, this.t_llinatge1,  this.t_llinatge2};
        
        person = new formPersona(fnaixement, fdef, dnaixement, ddef, nom, this.t_comentaris,
                this.c_sexe, this.l_);
        
        try {
            person.fill(p);
        } catch (DBException ex) {
            ex.show();
            Logger.getLogger(ModifyPerson.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

