    private static String nullify(String in){
        if (in==null || in.isEmpty()){
            return null;
        }else{
            return in;
        }
    }
    private static String nullify(javax.swing.JComboBox in){
        if (in.getSelectedIndex()==0){
            return null;
        }else{
            return String.valueOf(in.getSelectedItem());
        }
    }
